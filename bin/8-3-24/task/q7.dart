// Write a dart program that takes a number as input and prints its
// multiplication table upto 10.
// Test Data:
// Input a number: 8
// Expected Output :
// 8 x 1 = 8
// 8 x 2 = 16
// 8 x 3 = 24
// ...8 x 10 = 80
main(){
  print("8 x 1 = ${8*1}");
  print("8 x 2 = ${8*2}");
  print("8 x 3 = ${8*3}");
  print("8 x 4 = ${8*4}");
  print("8 x 5 = ${8*5}");
  print("8 x 6 = ${8*6}");
  print("8 x 7 = ${8*7}");
  print("8 x 8 = ${8*8}");
  print("8 x 9 = ${8*9}");
  print("8 x 10 = ${8*10}");
}